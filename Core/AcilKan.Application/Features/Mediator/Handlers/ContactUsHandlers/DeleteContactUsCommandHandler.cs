using AcilKan.Application.Features.Mediator.Commands.ContactUsCommands;
using AcilKan.Application.Interfaces;
using AcilKan.Domain.Entities;
using MediatR;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AcilKan.Application.Features.Mediator.Handlers.ContactHandlers
{
    public class DeleteContactUsCommandHandler(IRepository<ContactUs> _repository) : IRequestHandler<DeleteContactUsCommand>
    {
        public async Task Handle(DeleteContactUsCommand request, CancellationToken cancellationToken)
        {
            var value = await _repository.GetByIdAsync(request.Id);
            await _repository.DeleteAsync(value);
        }
    }
}
